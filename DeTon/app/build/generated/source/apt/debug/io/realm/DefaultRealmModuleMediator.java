package io.realm;


import android.util.JsonReader;
import io.realm.exceptions.RealmException;
import io.realm.internal.ImplicitTransaction;
import io.realm.internal.RealmObjectProxy;
import io.realm.internal.RealmProxyMediator;
import io.realm.internal.Table;
import java.io.IOException;
import java.util.ArrayList;
import java.util.Collections;
import java.util.List;
import java.util.Map;
import org.json.JSONException;
import org.json.JSONObject;
import com.example.anh.deton.Model.ChapterModel;
import com.example.anh.deton.Model.ConfigModel;
import com.example.anh.deton.Model.PageNumberModel;

@io.realm.annotations.RealmModule
class DefaultRealmModuleMediator extends RealmProxyMediator {

    private static final List<Class<? extends RealmObject>> MODEL_CLASSES;
    static {
        List<Class<? extends RealmObject>> modelClasses = new ArrayList<Class<? extends RealmObject>>();
        modelClasses.add(ConfigModel.class);
        modelClasses.add(PageNumberModel.class);
        modelClasses.add(ChapterModel.class);
        MODEL_CLASSES = Collections.unmodifiableList(modelClasses);
    }

    @Override
    public Table createTable(Class<? extends RealmObject> clazz, ImplicitTransaction transaction) {
        checkClass(clazz);

        if (clazz.equals(ConfigModel.class)) {
            return ConfigModelRealmProxy.initTable(transaction);
        } else if (clazz.equals(PageNumberModel.class)) {
            return PageNumberModelRealmProxy.initTable(transaction);
        } else if (clazz.equals(ChapterModel.class)) {
            return ChapterModelRealmProxy.initTable(transaction);
        } else {
            throw getMissingProxyClassException(clazz);
        }
    }

    @Override
    public void validateTable(Class<? extends RealmObject> clazz, ImplicitTransaction transaction) {
        checkClass(clazz);

        if (clazz.equals(ConfigModel.class)) {
            ConfigModelRealmProxy.validateTable(transaction);
        } else if (clazz.equals(PageNumberModel.class)) {
            PageNumberModelRealmProxy.validateTable(transaction);
        } else if (clazz.equals(ChapterModel.class)) {
            ChapterModelRealmProxy.validateTable(transaction);
        } else {
            throw getMissingProxyClassException(clazz);
        }
    }

    @Override
    public List<String> getFieldNames(Class<? extends RealmObject> clazz) {
        checkClass(clazz);

        if (clazz.equals(ConfigModel.class)) {
            return ConfigModelRealmProxy.getFieldNames();
        } else if (clazz.equals(PageNumberModel.class)) {
            return PageNumberModelRealmProxy.getFieldNames();
        } else if (clazz.equals(ChapterModel.class)) {
            return ChapterModelRealmProxy.getFieldNames();
        } else {
            throw getMissingProxyClassException(clazz);
        }
    }

    @Override
    public String getTableName(Class<? extends RealmObject> clazz) {
        checkClass(clazz);

        if (clazz.equals(ConfigModel.class)) {
            return ConfigModelRealmProxy.getTableName();
        } else if (clazz.equals(PageNumberModel.class)) {
            return PageNumberModelRealmProxy.getTableName();
        } else if (clazz.equals(ChapterModel.class)) {
            return ChapterModelRealmProxy.getTableName();
        } else {
            throw getMissingProxyClassException(clazz);
        }
    }

    @Override
    public <E extends RealmObject> E newInstance(Class<E> clazz) {
        checkClass(clazz);

        if (clazz.equals(ConfigModel.class)) {
            return clazz.cast(new ConfigModelRealmProxy());
        } else if (clazz.equals(PageNumberModel.class)) {
            return clazz.cast(new PageNumberModelRealmProxy());
        } else if (clazz.equals(ChapterModel.class)) {
            return clazz.cast(new ChapterModelRealmProxy());
        } else {
            throw getMissingProxyClassException(clazz);
        }
    }

    @Override
    public List<Class<? extends RealmObject>> getModelClasses() {
        return MODEL_CLASSES;
    }

    @Override
    public Map<String, Long> getColumnIndices(Class<? extends RealmObject> clazz) {
        checkClass(clazz);

        if (clazz.equals(ConfigModel.class)) {
            return ConfigModelRealmProxy.getColumnIndices();
        } else if (clazz.equals(PageNumberModel.class)) {
            return PageNumberModelRealmProxy.getColumnIndices();
        } else if (clazz.equals(ChapterModel.class)) {
            return ChapterModelRealmProxy.getColumnIndices();
        } else {
            throw getMissingProxyClassException(clazz);
        }
    }

    @Override
    public <E extends RealmObject> E copyOrUpdate(Realm realm, E obj, boolean update, Map<RealmObject, RealmObjectProxy> cache) {
        // This cast is correct because obj is either 
        // generated by RealmProxy or the original type extending directly from RealmObject
        @SuppressWarnings("unchecked") Class<E> clazz = (Class<E>) ((obj instanceof RealmObjectProxy) ? obj.getClass().getSuperclass() : obj.getClass());

        if (clazz.equals(ConfigModel.class)) {
            return clazz.cast(ConfigModelRealmProxy.copyOrUpdate(realm, (ConfigModel) obj, update, cache));
        } else if (clazz.equals(PageNumberModel.class)) {
            return clazz.cast(PageNumberModelRealmProxy.copyOrUpdate(realm, (PageNumberModel) obj, update, cache));
        } else if (clazz.equals(ChapterModel.class)) {
            return clazz.cast(ChapterModelRealmProxy.copyOrUpdate(realm, (ChapterModel) obj, update, cache));
        } else {
            throw getMissingProxyClassException(clazz);
        }
    }

    @Override
    public <E extends RealmObject> E createOrUpdateUsingJsonObject(Class<E> clazz, Realm realm, JSONObject json, boolean update)
        throws JSONException {
        checkClass(clazz);

        if (clazz.equals(ConfigModel.class)) {
            return clazz.cast(ConfigModelRealmProxy.createOrUpdateUsingJsonObject(realm, json, update));
        } else if (clazz.equals(PageNumberModel.class)) {
            return clazz.cast(PageNumberModelRealmProxy.createOrUpdateUsingJsonObject(realm, json, update));
        } else if (clazz.equals(ChapterModel.class)) {
            return clazz.cast(ChapterModelRealmProxy.createOrUpdateUsingJsonObject(realm, json, update));
        } else {
            throw getMissingProxyClassException(clazz);
        }
    }

    @Override
    public <E extends RealmObject> E createUsingJsonStream(Class<E> clazz, Realm realm, JsonReader reader)
        throws IOException {
        checkClass(clazz);

        if (clazz.equals(ConfigModel.class)) {
            return clazz.cast(ConfigModelRealmProxy.createUsingJsonStream(realm, reader));
        } else if (clazz.equals(PageNumberModel.class)) {
            return clazz.cast(PageNumberModelRealmProxy.createUsingJsonStream(realm, reader));
        } else if (clazz.equals(ChapterModel.class)) {
            return clazz.cast(ChapterModelRealmProxy.createUsingJsonStream(realm, reader));
        } else {
            throw getMissingProxyClassException(clazz);
        }
    }

}
